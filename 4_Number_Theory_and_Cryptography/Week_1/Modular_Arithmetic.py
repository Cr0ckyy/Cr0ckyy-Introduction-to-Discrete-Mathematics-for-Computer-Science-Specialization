# To find the remainder of the given expressions when divided by 3 and 5, we can use the modulo operator (%).

# Calculate the remainder of the first expression when divided by 3
remainder_q1 = (77 + 95 * 79) * 82 % 3

# Calculate the remainder of the second expression when divided by 5
remainder_q2 = (34 - 14 * 25) * (23 * 18 + 87) % 5

# Print the results on separate lines with labels
print("Remainder of the first expression when divided by 3:", remainder_q1)
print("Remainder of the second expression when divided by 5:", remainder_q2)
